import UIKit

extension UIColor {
    
    public static var ultraLight: UIColor {
        return UIColor.init(hue: 0, saturation: 0, brightness: 0, alpha: 0.03)
    }
    
    public static var veryUltraLight: UIColor {
        return UIColor.init(hue: 0, saturation: 0, brightness: 0, alpha: 0.02)
    }
}

